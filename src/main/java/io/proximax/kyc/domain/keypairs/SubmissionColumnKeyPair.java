package io.proximax.kyc.domain.keypairs;

public class SubmissionColumnKeyPair {

    private String label;
    private String key;

    public void setLabel(String title) {
        this.label = title;
    }
    public String getLabel() {
        return this.label;
    }

    public void setKey(String key) {
        this.key = key;
    }
    public String getKey() {
        return this.key;
    }
}
